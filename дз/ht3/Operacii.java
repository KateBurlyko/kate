


public class Operacii {
    /**
     * counting summ of 2 number
     *
     * @param a first number of sum
     * @param b second number
     * @return summ of 2numbers
     */
    double sum(double a, double b) {
        return a + b;
    }

    /**
     * counting differense of 2 number
     *
     * @param a first nubber of sum
     * @param b second number
     * @return differense of 2numbers
     */
    double raz(double a, double b) {
        return a - b;
    }

    double umn(double a, double b) {
        return a * b;
    }

    double del(double a, double b) {

        return a / b;
    }
}

