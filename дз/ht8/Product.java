package ht8;

/**
 * contain fiel from arraylist
 */
public class Product {
    String type;
    String name;
    int quantity;
    double price;

    /**
     *
     * @return type of product from arraylist
     */
    public String getType() {
        return type;
    }

    /**
     *
     * @return name of product from arraylist
     */
    public String getName() { return name; }

    /**
     *
     * @return type of product from arraylist
     */
    public int getQuantity() {
        return quantity;
    }

    /**
     *
     * @return type of product from arraylist
     */
    public double getPrice() {
        return price;
    }

}
